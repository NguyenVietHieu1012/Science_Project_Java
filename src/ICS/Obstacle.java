package ICS;

public class Obstacle {
    public double xmin = 0;
    public double xmax = 10;
    public double ymin = 0;
    public double ymax = 10;

    public Obstacle(double xmin, double xmax, double ymin, double ymax) {
        this.xmin = xmin;
        this.xmax = xmax;
        this.ymin = ymin;
        this.ymax = ymax;
    }
}
